									 <h2>BUDGETING</h2>
									 <h5>Sept 19, 2018</h5>
									 <h3>3 practical tips to evolve from an annual ritual to a powerful tool to generate better Business Results</h3>
									 <p>This article might be useful to you if you are an Owner Manager of a growing business with ~$5mn to $50mn revenues. If you need help with driving better business results by strengthening your planning and forecasting function, this article will have some practical ‘To-Dos’ for you</p>
									 <p>The Budget exercise is one of those things which ‘happen’ every year; a ritual which Business While owners recognise the need, they are left feeling somewhat dis-satisfied about the actual outcomes. The Budget (or any other planning tool you employ, like a Forecast) is not just an Excel spreadsheet but a guiding document that gives you a real shot at moving your enterprise in the right direction at the right pace. Make your budget reflect where you want your business to go and how you want it to grow. Take a conscious step back; see where you are and then visualize where you want to be. Or where you can be. Your budget is a map. Here are some pointers as to how you could draw it.</p>
									 <p>While planning your budget, don’t just look at the existing P&L and balance sheet numbers, though it is a good place to start. Don’t ask if you can increase your numbers by 5% -10% from the last year. Incrementalism is an enemy of realising your business’ full potential. Understand your market and analyse where you are in it. How much of the market share does you company/organization have? How much of the market share do you want to capture for this year? For most small businesses, the market share may be possibly 0.5% or 1%, or even lesser, of industry revenues. Thinking of ways to increase market share to say 1.5% – 2% will create a roadmap to grow your business by five or six times. The problem with starting with the ‘last years’ numbers as the base is that managers involved with the planning process, also start with the existing set of constraints that the business is challenged with. While there is little doubt that any results of an opportunity led thought process will need to be married with the ground realities at some point in the planning process, there is considerable merit in the first set of budget number being driven by a ‘opportunity’ thought process rather than an incremental (constraint led) thought process</p>
									 <p>Incorporate Business Drivers measurement as a cornerstone of your Budget</p>
									 <p>Too many Business Owners and CFO’s are guilty of simply looking only at the financial statements as the start and end of the planning process. Budgeting is a business exercise not a financial ritual. We need to realize that accounting and financial metrics are merely a by-product of business metrics. Think about key drivers for your business unit. Break down the drivers to better understand all the factors that influence it. For example, sales drivers include sales volumes, pricing growth, customer stickiness, annuity in sales, number of sales channels, geographies that you are active in and those that you want to be active in. Productivity metrics include sales per salesperson, revenue per month, returns on marketing spends, and Life Time Value of a customer. Unless we really start planning for business drivers, it’s hard to map what the P&L and balance sheet will look like at the end of the year. For most businesses, starting with what your customers are likely to do in the coming year, is a great starting point to the planning exercise. Measuring your existing share of pocket in key clients, for instance, and planning for the higher share of pocket at end of the year, is a great way to drive conversations around the strategy around customer retention, upselling and cross-selling, better differentiation, competitor analysis and benchmarking. The ideas and execution strategies arising from internal conversations on these matters are valuable (and the true) outcomes of a planning process, rather than a set of ‘numbers’</p>
									 <p>Assign clear accountability for execution of the Plan</p>
									 <p>Every metric in the plan needs to have an owner. Every plan needs to be ‘wrapped around’ the organisation structure in your enterprise. Most plans fail because of lack of adequate detailing around clear ownership, Key Result Areas and KPIs for all key positions and the frequency of review. Finance teams need to track progress against business drivers and financial outcomes on a weekly, fortnightly or monthly basis. The biggest benefit of regular reviews is alignment between internal stakeholders on planning learnings and to determine what and how quickly to pivot. Think through the interdependencies when crafting your plan; poor response times to fix failures in inter-dependencies is the number one cause for plans to fail. In most enterprises, Finance is tasked with completion of the Budget, but accountability for plan assumptions and outcomes rests with Business teams. Finance needs to take ownership for measurement, red-flagging and ensuring that the plan ‘hangs together’.</p>
									 <p>Thinking of budgeting as a business exercise helps you deal with your business as an living, morphing entity. In an increasingly uncertain business environment, the one thing (in your control) which help you like a lighthouse in a storm is your plan. The annual Budget does however have its limitations. Several enterprises find that plan assumptions made before or at the start of the year are no longer valid by the middle of the year. Evolved organizations are therefore increasingly adopting more iterative and ‘real time’ tools like forecasts as their primary tool for planning. More about forecasting in our next article. Watch this space.</p>
